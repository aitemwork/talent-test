const TIMER_SEC = 25;

function calcScores(ans) {
  const s = {};
  QUESTIONS.forEach((q,i) => {
    const c = ans[i]; if(!c) return;
    Object.entries(q[c]).forEach(([t,p]) => { s[t]=(s[t]||0)+p; });
  });
  return s;
}

function calcBlocks(sorted) {
  const rank = {}; sorted.forEach(([t],i) => { rank[t]=40-i; });
  return Object.entries(BLOCKS)
    .map(([b,{talents,emoji}]) => [b, emoji, talents.reduce((s,t)=>s+(rank[t]||0),0)])
    .sort((a,b)=>b[2]-a[2]);
}

async function sendTg(name, phone, sorted, blocks) {
  const top5  = sorted.slice(0,5).map(([t,s],i)=>`${i+1}. ${t} — ${s.toFixed(1)}`).join("\n");
  const top10 = sorted.slice(0,10).map(([t,s],i)=>`${i+1}. ${t} — ${s.toFixed(1)}`).join("\n");
  const all40 = sorted.map(([t,s],i)=>`${i+1}. ${t} — ${s.toFixed(1)}`).join("\n");
  const blk   = blocks.map(([b,e,s],i)=>`${i+1}. ${e} ${b} — ${s} очков`).join("\n");
  const msg = `🌿 <b>НОВЫЙ РЕЗУЛЬТАТ</b>\n\n👤 <b>${name}</b>\n📞 ${phone}\n\n⭐ <b>ТОП-5</b>\n${top5}\n\n🔟 <b>ТОП-10</b>\n${top10}\n\n📊 <b>ВСЕ 40 ТАЛАНТОВ</b>\n${all40}\n\n🧩 <b>БЛОКИ</b>\n${blk}`;
  try {
    await fetch("/api/send", {
      method:"POST", headers:{"Content-Type":"application/json"},
      body: JSON.stringify({ text: msg })
    });
  } catch(e){ console.error(e); }
}

